#!/usr/bin/env python3
"""A module to add two floats toghether"""


def add(a: float, b: float) -> float:
    """
    A funcion to add two floats anf return a float value
    Args:
        a(float): the first float
        b(float): the second float
    Return: The addition of both floats in sucess
    """
    return a + b
